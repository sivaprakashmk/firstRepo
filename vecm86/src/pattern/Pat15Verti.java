package pattern;

public class Pat15Verti {
	public static void main(String[] args) {
		
		int n=5,a1=1,a2=6,a3=11;
		char ch1='a',ch2='f';
		for (int i = 0; i <n; i++) {
				System.out.print(a1+" ");
				a1++;
				System.out.print(ch1+" ");
				ch1++;
				System.out.print(a2+" ");
				a2++;
				System.out.print(ch2+" ");
				ch2++;
				System.out.print(a3+" ");
				a3++;
				System.out.println();
		}
	}	
}
