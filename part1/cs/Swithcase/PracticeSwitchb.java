class PracticeSwitchb
{
	public static void main(String[] args)
	{
		int a = 10, b = 20, c = 30, d = 40;
		switch(b)
		{
		case 10:
			{
				System.out.println(a+ "is 10 "); 
			}
			break;
		case 20:
			{
				System.out.println(b+ "is 20 "); 
			}
			break;
		case 30:
			{
				System.out.println(c+ "is 30 "); 
			}
			break;
		case 40:
			{
				System.out.println(d+ "is 40 ");
			}
			break;
		}
	}  
}		